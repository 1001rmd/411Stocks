package models;


public class BoardRequest{

	public String name;
	
	public double start;

	
	public void setName(String name){
		this.name = name;
	}

	public void setStart(){
		this.start = start;
	}
	
	public String getName(){
		return name;
	}
	
	public double getStart(){
		return start;
	}
	
}